#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		mod;
	char	c;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n *= -1;
		}
		mod = n % 10;
		if (n / 10 >= 1)
		{
			n = n / 10;
			ft_putnbr_fd(n, fd);
		}
		c = '0' + mod;
		write(fd, &c, 1);
	}
}
