#include "libft.h"

void	ft_bzero(void *p, size_t n)
{
	char	*p_tem;
	size_t	i;

	i = 0;
	p_tem = p;
	while (i < n)
	{
		*p_tem = '\0';
		p_tem++;
		i++;
	}
}
